function setup() {
	//create a place to draw
	createCanvas(640, 360);
	noStroke();
	noLoop();
}

function draw() {
	//clear the background
	background(90, 90, 110);

	//draw the rectangle
	fill(0, 0, 0);
	rect(40, 40, 200, 280);

	//draw the x
	fill(150, 45, 45);
	rect(30, 20, 100, 10, 20);







}
